/*
Create a program which prints the sum of two numbers.
*/

#include <stdio.h>

int main() {

    // Declare two integer variables
    int a = 5;
    int b = 10;
    int sum = a + b; // Calculate the sum of two numbers

    printf("The sum of %d and %d is: %d\n", a, b, sum); // Prints the sum of two numbers

    return 0;
}